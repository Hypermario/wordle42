int	ft_numlen(int nb)
{
	int				i;
	unsigned int	n;

	i = 0;
	if (nb == 0)
		return (0);
	if (nb < 0)
		n = -nb;
	else
		n = nb;
	while (n > 0)
	{
		n /= 10;
		i++;
	}
	return (i - 1);
}
